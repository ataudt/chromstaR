check.experiment.table <- function(experiment.table) {
    if (!is.data.frame(experiment.table)) return(1)
    if (any(names(experiment.table) != c('file', 'mark', 'condition', 'replicate', 'pairedEndReads'))) return(2)
    return(0)
}

